"use strict";
// Assigning and Updating Variables: Create a function where you declare a variable using 
// let and assign an initial value. Then, update its value within the same function and 
// log both the initial and updated values.
function updateVariable() {
    let number = 10;
    console.log("initial value:", number);
    number = 20;
    console.log("update value:", number);
}
updateVariable();
